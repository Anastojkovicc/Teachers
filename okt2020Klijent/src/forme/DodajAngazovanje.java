    private void srediKomboNastavnika() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacija.VRATI_NASTAVNIKE);
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        ArrayList<Nastavnik> listaNastavnika = (ArrayList<Nastavnik>) so.getOdgovor();
        cmbNastavnik.removeAllItems();

        for (Nastavnik nastavnik : listaNastavnika) {
            cmbNastavnik.addItem(nastavnik);
        }
    }

    private void srediKomboPredmeta() {
        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacija.VRATI_PREDMETE);
        KomunikacijaSaServerom.getInstanca().posaljiZahtev(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstanca().primiOdgovor();
        ArrayList<Predmet> listaPredmeta = (ArrayList<Predmet>) so.getOdgovor();
        cmbPredmet.removeAllItems();

        for (Predmet predmet : listaPredmeta) {
            cmbPredmet.addItem(predmet);
        }
    }

    private void srediKomboPrograma() {
        cmbStudijskiProgram.removeAllItems();
        for (StudijskiProgram value : StudijskiProgram.values()) {
            cmbStudijskiProgram.addItem(value);
        }
    }
