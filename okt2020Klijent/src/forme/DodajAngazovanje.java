    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        if (txtSemestarr.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti semestar");
            return;
        }
       
        int semestar = Integer.parseInt(txtSemestarr.getText());
        if(semestar<1 || semestar>8){
         JOptionPane.showMessageDialog(this, "Semestar mora imati vrednost od 1 do 8");
            return;
        }
        Nastavnik n = (Nastavnik) cmbNastavnik.getSelectedItem();
        Predmet p = (Predmet) cmbPredmet.getSelectedItem();
        StudijskiProgram sp = (StudijskiProgram) cmbStudijskiProgram.getSelectedItem();
//        SimpleDateFormat sdf= new SimpleDateFormat("dd.MM.yy");
        Date datum = new Date();
       
//        Date dat= sdf.parse(datum);
        PredajePredmet pp= new PredajePredmet(n, p, semestar, sp, datum, ulogovani);
        
        KlijentskaForma kf= (KlijentskaForma) getParent();
        kf.dodajUTabelu(pp);
    }//GEN-LAST:event_btnDodajActionPerformed

